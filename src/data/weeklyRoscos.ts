// Sopalo necesita un set de emojis DISTINTO por cada día de la semana (no
// uno solo para toda la semana como el bonus rosco original), así que
// rota el índice por día además de por semana — 7 sets consumidos por
// semana de los 11 disponibles, sin repetir dentro de la misma semana.
export const getBonusSetForDay = (
  dayIndex: number,
  referenceDate = new Date(),
  language = "es"
): RoscoEntry[] => {
  const registry = BONUS_ROSCO_REGISTRY[language] ?? BONUS_ROSCO_REGISTRY["es"];
  const cycleSize = registry.length;
  const weekStart = getWeekStart(referenceDate);
  const weeksSinceBase = Math.floor(
    (new Date(weekStart).getTime() - new Date(CYCLE_BASE_DATE).getTime()) / MS_PER_WEEK,
  );
  const idx = (((weeksSinceBase * 7 + dayIndex) % cycleSize) + cycleSize) % cycleSize;
  return registry[idx];
};

